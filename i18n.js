// Change language function
function setLanguage(langKey) {
    if (!translations[langKey]) return;
    
    // Save selection
    localStorage.setItem('alya_lang', langKey);
    document.documentElement.lang = langKey;

    // Translate all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[langKey][key]) {
            el.innerText = translations[langKey][key];
        }
    });

    // Translate input placeholders with data-i18n-ph
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (translations[langKey][key]) {
            el.placeholder = translations[langKey][key];
        }
    });

    // Update active state in selector
    document.querySelectorAll('.lang-option').forEach(opt => {
        opt.classList.remove('active');
        if (opt.getAttribute('data-lang') === langKey) {
            opt.classList.add('active');
        }
    });

    // Update current selected label
    const activeLabel = document.getElementById('current-lang-code');
    if (activeLabel) {
        activeLabel.innerText = langKey.toUpperCase();
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('alya_lang') || 'en';
    setLanguage(savedLang);
});
